package Day4;

public class IfElseCondition {
    public static void main(String[] args){

        //if Conditional Statement
        int person_age1 = 19;
        if (person_age1 >= 18){
            System.out.println("Eligible for Vote");
        }

        //if else Conditional Statement
        int person_age2 = 17;
        if (person_age2>=18){
            System.out.println("Eligible for vote");
        } else {
            System.out.println("Note Eligible for vote");
        }

    }
}
