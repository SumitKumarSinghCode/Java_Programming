package Day14;

/*
Variable:-
1) When a variable is declared as final, its value cannot be changed once it is assigned.
2) A final variable must be initialized at the time of declaration or in the constructor.

 Method:-
1) When a method is declared as final, it cannot be overridden by subclasses.
2) This is useful when we want to prevent modification of a method's behavior in derived classes.

Class:-
1) When a class is declared as final, it cannot be subclassed or extended.
2) This is useful when you want to create an immutable class or prevent inheritance for security or design reasons.
 */
